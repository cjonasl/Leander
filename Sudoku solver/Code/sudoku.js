window.sudoku.solveSudoku = function () {
    var obj, result;

    $("input[data-startinteger='1']").removeAttr("data-startinteger");

    obj = window.sudoku.readInputIntegers();

    if (obj.errorMsg) {
        alert(obj.errorMsg);
    }
    else {
        result = window.sudoku.run(obj.inputSudokuBrd);
        alert(result);
    }
};
